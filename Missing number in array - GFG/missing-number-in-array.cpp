class Solution{
  public:
    int MissingNumber(vector<int>& array, int n) {
        long long int ans1=1;
        long long int ans2=array[0];
        for(int i=1;i<array.size();i++)
        {
            ans1= ans1^(i+1);
            ans2= ans2^array[i];
        }
        ans1= ans1^n;
        return ans1^ans2;
    }
};

